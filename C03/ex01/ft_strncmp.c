#include <unistd.h>

int	ft_strncmp(char *s1, char *s2, unsigned int n)
{
	unsigned int	i;

	if (n == 0)
		return (0);
	i = 0;
	while (s1[i] == s2[i] && s1[i] != '\n' && s2[i] != '\n' && i < n)
	{
		i++;
	}
	if (i == n)
	{
		i--;
	}
	return (s1[i] - s2[i]);
}
